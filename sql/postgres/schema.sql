CREATE VIEW customer_orders_unified AS
SELECT
  'booking'::text AS record_type,
  b.id AS record_id,
  COALESCE(b.booking_code, 'FG-' || LPAD(b.id::text, 6, '0')) AS record_code,
  b.user_id,
  b.customer_name,
  b.phone,
  b.email,
  r.room_name AS item_label,
  b.status,
  b.payment_status,
  b.payment_method,
  b.final_total AS total_amount,
  b.paid_amount,
  b.loyalty_points_earned,
  b.loyalty_points_redeemed,
  b.loyalty_discount,
  b.created_at,
  b.updated_at,
  '/my-bookings'::text AS action_url
FROM bookings b
LEFT JOIN rooms r ON r.id = b.room_id
UNION ALL
SELECT
  'store_order'::text AS record_type,
  so.id AS record_id,
  so.order_code AS record_code,
  so.user_id,
  so.customer_name,
  so.phone,
  so.email,
  'Store order'::text AS item_label,
  so.status,
  so.payment_status,
  so.payment_method,
  so.total_amount,
  so.paid_amount,
  so.loyalty_points_earned,
  so.loyalty_points_redeemed,
  so.loyalty_discount,
  so.created_at,
  so.updated_at,
  '/my-bookings'::text AS action_url
FROM store_orders so;

