BEGIN;

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS trigger AS $$
BEGIN
  NEW.updated_at = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP VIEW IF EXISTS customer_orders_unified;
DROP TABLE IF EXISTS audit_log, admin_notifications, site_notifications, smart_notification_events,
  chatbot_messages, chatbot_sessions, smart_report_snapshots, complaint_messages, complaints,
  booking_items, bookings, store_order_items, store_orders, store_products, menu_items,
  time_slots, rooms, site_users, admins, business_hours, system_settings CASCADE;

CREATE TABLE admins (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username varchar(50) NOT NULL UNIQUE,
  password varchar(255) NOT NULL,
  full_name varchar(100) NOT NULL,
  role varchar(20) NOT NULL DEFAULT 'employee',
  phone varchar(20),
  email varchar(100),
  status varchar(20) NOT NULL DEFAULT 'Active',
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE rooms (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  room_name varchar(100) NOT NULL,
  room_type varchar(50) NOT NULL,
  price_per_hour numeric(10,2) NOT NULL,
  status varchar(20) NOT NULL DEFAULT 'Available',
  services text,
  description text,
  image_path varchar(255),
  image_uploaded_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE site_users (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  full_name varchar(120) NOT NULL,
  email varchar(150) NOT NULL UNIQUE,
  phone varchar(20),
  profile_image varchar(255),
  password_reset_token varchar(128),
  password_reset_expires_at timestamptz,
  password varchar(255) NOT NULL,
  role varchar(20) NOT NULL DEFAULT 'user',
  loyalty_points integer NOT NULL DEFAULT 0,
  status varchar(20) NOT NULL DEFAULT 'Active',
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_site_users_role_status ON site_users(role, status);
CREATE INDEX idx_site_users_password_reset_token ON site_users(password_reset_token);

CREATE TABLE business_hours (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  day_of_week varchar(12) NOT NULL UNIQUE,
  is_open boolean NOT NULL DEFAULT true,
  opening_time time NOT NULL DEFAULT '09:00:00',
  closing_time time NOT NULL DEFAULT '23:59:00',
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CHECK (day_of_week IN ('Monday','Tuesday','Wednesday','Thursday','Friday','Saturday','Sunday'))
);

CREATE TABLE menu_items (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  item_name varchar(100) NOT NULL,
  item_category varchar(50) NOT NULL,
  item_price numeric(10,2) NOT NULL,
  item_description text,
  is_available boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE store_products (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_name varchar(150) NOT NULL,
  category varchar(100) NOT NULL,
  price numeric(10,2) NOT NULL,
  description text,
  image_path varchar(255),
  stock_quantity integer NOT NULL DEFAULT 0,
  status varchar(20) NOT NULL DEFAULT 'Active',
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_store_products_status_category ON store_products(status, category);

CREATE TABLE system_settings (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  setting_key varchar(100) NOT NULL UNIQUE,
  setting_value text NOT NULL,
  setting_type varchar(20) NOT NULL DEFAULT 'string',
  description text,
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE time_slots (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  room_id integer REFERENCES rooms(id) ON DELETE CASCADE,
  slot_time time NOT NULL,
  slot_label varchar(20) NOT NULL,
  is_active boolean NOT NULL DEFAULT true,
  UNIQUE (room_id, slot_time)
);
CREATE INDEX idx_time_slots_active_time ON time_slots(is_active, slot_time);

CREATE TABLE bookings (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  booking_code varchar(40) UNIQUE,
  customer_name varchar(100) NOT NULL,
  phone varchar(20) NOT NULL,
  email varchar(150),
  customer_session_token varchar(64),
  ticket_token varchar(255) UNIQUE,
  user_id integer REFERENCES site_users(id) ON DELETE SET NULL,
  room_id integer NOT NULL REFERENCES rooms(id) ON DELETE CASCADE,
  booking_date date NOT NULL,
  start_time time NOT NULL,
  hours integer NOT NULL,
  end_time time GENERATED ALWAYS AS (start_time + (hours * interval '1 hour')) STORED,
  total_price numeric(10,2) NOT NULL,
  additional_items_total numeric(10,2) DEFAULT 0.00,
  final_total numeric(10,2) GENERATED ALWAYS AS (total_price + COALESCE(additional_items_total, 0)) STORED,
  loyalty_points_earned integer NOT NULL DEFAULT 0,
  loyalty_points_redeemed integer NOT NULL DEFAULT 0,
  loyalty_discount numeric(10,2) NOT NULL DEFAULT 0.00,
  status varchar(20) NOT NULL DEFAULT 'Pending',
  payment_status varchar(20) DEFAULT 'Unpaid',
  payment_method varchar(20),
  paid_amount numeric(10,2) DEFAULT 0.00,
  notes text,
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_customer_session_token ON bookings(customer_session_token);
CREATE INDEX idx_booking_user_id ON bookings(user_id);
CREATE INDEX idx_booking_datetime ON bookings(room_id, booking_date, start_time, end_time);

CREATE TABLE booking_items (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  booking_id integer NOT NULL REFERENCES bookings(id) ON DELETE CASCADE,
  menu_item_id integer NOT NULL REFERENCES menu_items(id) ON DELETE CASCADE,
  quantity integer NOT NULL DEFAULT 1,
  item_price numeric(10,2) NOT NULL,
  item_total numeric(10,2) GENERATED ALWAYS AS (quantity * item_price) STORED,
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_booking_items_booking ON booking_items(booking_id);
CREATE INDEX idx_booking_items_menu_item ON booking_items(menu_item_id);

CREATE TABLE store_orders (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_code varchar(40) NOT NULL UNIQUE,
  user_id integer NOT NULL REFERENCES site_users(id) ON DELETE CASCADE,
  customer_name varchar(120) NOT NULL,
  phone varchar(20),
  email varchar(150),
  subtotal numeric(10,2) NOT NULL DEFAULT 0.00,
  loyalty_points_redeemed integer NOT NULL DEFAULT 0,
  loyalty_discount numeric(10,2) NOT NULL DEFAULT 0.00,
  total_amount numeric(10,2) NOT NULL DEFAULT 0.00,
  loyalty_points_earned integer NOT NULL DEFAULT 0,
  payment_status varchar(20) NOT NULL DEFAULT 'Unpaid',
  payment_method varchar(20),
  paid_amount numeric(10,2) NOT NULL DEFAULT 0.00,
  status varchar(20) NOT NULL DEFAULT 'Pending',
  stock_deducted boolean NOT NULL DEFAULT false,
  notes text,
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_store_orders_user_created ON store_orders(user_id, created_at);
CREATE INDEX idx_store_orders_status_payment ON store_orders(status, payment_status);

CREATE TABLE store_order_items (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id integer NOT NULL REFERENCES store_orders(id) ON DELETE CASCADE,
  product_id integer REFERENCES store_products(id) ON DELETE SET NULL,
  product_name varchar(150) NOT NULL,
  category varchar(100),
  quantity integer NOT NULL DEFAULT 1,
  item_price numeric(10,2) NOT NULL,
  item_total numeric(10,2) GENERATED ALWAYS AS (quantity * item_price) STORED,
  created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_store_order_items_order ON store_order_items(order_id);
CREATE INDEX idx_store_order_items_product ON store_order_items(product_id);

CREATE TABLE complaints (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  complaint_code varchar(40) UNIQUE,
  user_id integer REFERENCES site_users(id) ON DELETE SET NULL,
  customer_session_token varchar(64),
  customer_name varchar(100) NOT NULL,
  customer_email varchar(150),
  phone varchar(20),
  message text NOT NULL,
  status varchar(20) NOT NULL DEFAULT 'Open',
