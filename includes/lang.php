<?php
if (!defined('SITE_DEFAULT_LANGUAGE')) {
    define('SITE_DEFAULT_LANGUAGE', 'en');
}

$site_supported_languages = ['en', 'ar'];

if (isset($_GET['lang']) && in_array($_GET['lang'], $site_supported_languages, true)) {
    $_SESSION['site_language'] = $_GET['lang'];
}

if (empty($_SESSION['site_language']) || !in_array($_SESSION['site_language'], $site_supported_languages, true)) {
    $_SESSION['site_language'] = SITE_DEFAULT_LANGUAGE;
}

function site_language() {
    return $_SESSION['site_language'] ?? SITE_DEFAULT_LANGUAGE;
}

function site_is_rtl() {
    return site_language() === 'ar';
}

function site_direction() {
    return site_is_rtl() ? 'rtl' : 'ltr';
}

function site_switch_language_url($language) {
    $query = $_GET;
    $query['lang'] = $language;
    $path = basename($_SERVER['PHP_SELF'] ?? 'index.php');

    return $path . (!empty($query) ? '?' . http_build_query($query) : '');
}

